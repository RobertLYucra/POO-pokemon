    public void MostrarPokemon(){
        if (jFPokemon.jRadioButton1.isSelected()){
            url = this.getClass().getResource("/Imagenes/Charizard.gif");
            ImageIcon icon = new ImageIcon(url);
            jLabel4.setIcon(icon);
            
            url = this.getClass().getResource("/Imagenes/Frogadier.gif");
            ImageIcon icon2 = new ImageIcon(url);
            jLabel5.setIcon(icon2);
        }
        else if(jFPokemon.jRadioButton2.isSelected()){
            url = this.getClass().getResource("/Imagenes/Gogoat.gif");
            ImageIcon icon = new ImageIcon(url);
            jLabel4.setIcon(icon);
            
            url = this.getClass().getResource("/Imagenes/Charizard.gif");
            ImageIcon icon2 = new ImageIcon(url);
            jLabel5.setIcon(icon2);
        }
        else if(jFPokemon.jRadioButton3.isSelected()){
            url = this.getClass().getResource("/Imagenes/Frogadier.gif");
            ImageIcon icon = new ImageIcon(url);
            jLabel4.setIcon(icon);
            
            url = this.getClass().getResource("/Imagenes/Gogoat.gif");
            ImageIcon icon2 = new ImageIcon(url);
            jLabel5.setIcon(icon2);
        }
    }
    public void cambiarNPokemon(){
        jLabel15.setText(jFPokemon.nombreR);
        
        if(jFCambiarNombrePokemon.jButton2.isSelected()){
            jLabel14.setText(jFPokemon.nombre);
        }
        else if(jFCambiarNombrePokemon.jButton1.isSelected()){
            jLabel14.setText(jFCambiarNombrePokemon.Cnombre);
           
        }
    }
