    public void Imagen(){
        if (jFEntrenador.jRadioButton1.isSelected()){
            jLabel1.setVisible(true);
            jLabel2.setVisible(false);
        }
        else if (jFEntrenador.jRadioButton2.isSelected()){
            jLabel1.setVisible(false);
            jLabel2.setVisible(true);
        }
    }
